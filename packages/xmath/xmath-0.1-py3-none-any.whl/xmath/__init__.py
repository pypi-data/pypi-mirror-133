import math

# xmath module

def sum(num_1,  num_2):
    return num_1 + num_2

def dif(num_1, num_2):
    return num_1 - num_2

def mul(num_1, num_2):
    return num_1 * num_2

def div(num_1, num_2):
    return num_1 / num_2

def floor(num_1, num_2):
    return num_1 // num_2

def sq(num):
    return num * num

def sqrt(num):
    sqrt = math.sqrt(num)
    return print(sqrt)

def cube(num):
    return num * num * num