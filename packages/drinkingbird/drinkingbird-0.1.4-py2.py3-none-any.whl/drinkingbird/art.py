def screen_clear():
    os.system('clear' if os.name == 'posix' else 'cls')


class Printer:
    def __init__(self):
        self.i = 0

    def __call__(self):
        screen_clear()
        sys.stdout.write(banner)
        sys.stdout.write(figs[self.i])
        sys.stdout.flush()
        self.i = (self.i + 1) % len(figs)
